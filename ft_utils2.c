#include "pipex.h"

size_t	ft_strlen(const char *str)
{
	int	len;

	len = 0;
	while (str[len] != '\0')
	{
		len++;
	}
	return (len);
}

char	*ft_strjoin(char const *s1, char const *s2)
{
	unsigned char	*ptr;
	unsigned int	size;
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	if(!s2)
		return ((char *)s1);
	size = ft_strlen(s1) + ft_strlen(s2);
	ptr = (unsigned char *)malloc((size + 1) * sizeof(char));
	if (!ptr)
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		ptr[i] = s2[j];
		i++;
		j++;
	}
	ptr[i] = '\0';
	return ((char *) ptr);
}
